def get_framework_knowledge() -> str:
    """Return a formatted string of all framework knowledge for prompt injection.

    This produces a comprehensive text block covering:
    - Metacoupling framework overview (intracoupling, pericoupling, telecoupling)
    - The five components with definitions and guiding questions
    - The 14 telecoupling categories with examples
    """
    sections: list[str] = []

    # --- Metacoupling overview ---
    sections.append("## METACOUPLING FRAMEWORK OVERVIEW\n")
    sections.append(
        "The metacoupling framework (Liu, 2017) is an integrated framework for "
        "understanding human-nature interactions across all spatial scales. It "
        "encompasses three types of couplings:\n"
    )
    for ct in CouplingType:
        sections.append(f"- **{ct.value.title()}**: {ct.description}\n")
    sections.append(
        "\nA coupled human and natural system (CHANS) contains interconnected "
        "human (socioeconomic) and natural (environmental/ecological) components "
        "that interact with each other.\n"
    )

    # --- Five components ---
    sections.append("\n## FIVE FRAMEWORK COMPONENTS\n")
    sections.append(
        "The telecoupling framework (Liu et al., 2013) identifies five "
        "interrelated components for analyzing distant human-nature interactions:\n"
    )
    for comp, defn in COMPONENT_DEFINITIONS.items():
        sections.append(f"\n### {defn.name}\n")
        sections.append(f"{defn.description}\n")
        sections.append("\nSub-elements:")
        for sub in defn.sub_elements:
            sections.append(f"  - {sub}")
        sections.append("\nGuiding questions:")
        for q in defn.guiding_questions:
            sections.append(f"  - {q}")
        sections.append("")

    # --- 14 telecoupling categories ---
    sections.append("\n## TELECOUPLING CATEGORIES\n")
    sections.append(
        "Liu et al. (2013) identified 14 major categories of telecoupling. "
        "A single study may involve one or more of these categories:\n"
    )
    for i, cat in enumerate(TELECOUPLING_CATEGORIES, 1):
        sections.append(f"{i}. **{cat.name}**: {cat.description}")
        sections.append(f"   Example: {cat.example}\n")

    # --- Key insights from Liu (2017) and Liu (2023) ---
    sections.append("\n## KEY ANALYTICAL INSIGHTS\n")

    sections.append("### Operationalization Procedure (Liu, 2017)\n")
    sections.append(
        "Operationalizing the metacoupling framework involves six phases:\n"
        "  1. Set research goals — formulate objectives, questions, hypotheses\n"
        "  2. Define focal system(s) — determined by research goals and feasibility\n"
        "  3. Review literature / conduct studies on flows, agents, causes, effects — "
        "trace flows across space to determine system boundaries\n"
        "  4. Finalize system boundaries — where flows start and end define boundaries; "
        "entities affecting flows are agents; reasons for flows are causes\n"
        "  5. Collect and analyze data — quantitative and qualitative\n"
        "  6. Communicate results — publications, stakeholder engagement\n"
    )

    sections.append("### Coupling Transformations (Liu, 2023)\n")
    sections.append(
        "Systems can undergo four stages of transformation:\n"
        "  - Noncoupling: no human-nature interactions within or between systems\n"
        "  - Coupling: interactions emerge among components\n"
        "  - Decoupling: interactions weaken or dissolve (e.g., due to shocks "
        "like economic recession, pandemic, or war)\n"
        "  - Recoupling: weakened or dissolved interactions are recovered\n"
        "These stages can occur in intracoupling, pericoupling, and telecoupling "
        "simultaneously or separately.\n"
    )

    sections.append("### Complex Interactions Among Coupling Types (Liu, 2023)\n")
    sections.append(
        "  - Intracoupling, pericoupling, and telecoupling can have synergistic effects\n"
        "  - Pericoupling and telecoupling can amplify intracoupling\n"
        "  - Increases in one type of coupling may reduce other types\n"
        "  - One type of coupling may generate benefits on one scale at the cost "
        "of other scales (spatial tradeoffs)\n"
        "  - Telecoupling, pericoupling, and intracoupling can interact indirectly\n"
        "  - Feedbacks (positive or negative) are common and may take time to emerge\n"
    )

