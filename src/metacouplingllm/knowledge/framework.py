class CouplingType(str, Enum):
    """Types of coupling in the metacoupling framework (Liu, 2017)."""

    INTRACOUPLING = "intracoupling"
    PERICOUPLING = "pericoupling"
    TELECOUPLING = "telecoupling"

    @property
    def description(self) -> str:
        return _COUPLING_DESCRIPTIONS[self]


_COUPLING_DESCRIPTIONS: dict[CouplingType, str] = {
    CouplingType.INTRACOUPLING: (
        "Human-nature interactions within a coupled human and natural system "
        "(Liu, 2017). The intracoupling framework focuses on interactions "
        "between human and nature subsystems within one focal system, through "
        "flows facilitated by agents, generated by causes, and leading to "
        "effects within both the focal system and any spillover systems."
    ),
    CouplingType.PERICOUPLING: (
        "Human-nature interactions between adjacent coupled human and natural "
        "systems (Liu, 2017). The pericoupling framework has the same "
        "structure as the telecoupling framework (systems, flows, agents, "
        "causes, effects) except that sending and receiving systems are "
        "nearby rather than far away. For example, flows of labor and "
        "materials between neighboring counties or nations."
    ),
    CouplingType.TELECOUPLING: (
        "Human-nature interactions between distant coupled human and natural "
        "systems (Liu et al., 2013). Telecoupling involves sending, receiving, "
        "and spillover systems connected by flows of information, energy, "
        "material, people, organisms, and capital across large geographic "
        "distances. Research shows that in many cases, telecouplings are "
        "stronger than pericouplings, challenging Tobler's First Law of "
        "Geography (Liu, 2023)."
    ),
}


class SystemRole(str, Enum):
    """Roles that a coupled human and natural system can play."""

    SENDING = "sending"
    RECEIVING = "receiving"
    SPILLOVER = "spillover"

    @property
    def description(self) -> str:
        return _SYSTEM_ROLE_DESCRIPTIONS[self]


_SYSTEM_ROLE_DESCRIPTIONS: dict[SystemRole, str] = {
    SystemRole.SENDING: (
        "A system that sends information, energy, material, people, "
        "organisms, and capital to other system(s). In intracoupling, "
        "the concept of sending system is not applicable — only the "
        "focal system is of interest (Liu, 2017, Table 1)."
    ),
    SystemRole.RECEIVING: (
        "A system that receives information, energy, material, people, "
        "organisms, and capital from sending system(s). In pericoupling, "
        "receiving systems are adjacent to sending systems; in telecoupling, "
        "they are distant (Liu, 2017, Table 1)."
    ),
    SystemRole.SPILLOVER: (
        "A system that is affected by or affects interactions between sending "
        "and receiving systems, even though it is not a direct participant. "
        "Spillover effects can be much larger than effects between sending "
        "and receiving systems (Liu, 2023). Spillover systems exist in all "
        "three coupling types and are often traditionally overlooked."
    ),
}


